package com.geneseeq.common.pojo.express;

public class ExpressCompanyResponseVO {

	private String id;
	private String name;
	private String typeofshipping;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTypeofshipping() {
		return typeofshipping;
	}

	public void setTypeofshipping(String typeofshipping) {
		this.typeofshipping = typeofshipping;
	}

}
